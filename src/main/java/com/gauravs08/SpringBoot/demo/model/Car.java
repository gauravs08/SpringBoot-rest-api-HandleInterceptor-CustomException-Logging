package com.gauravs08.SpringBoot.demo.model;
/**
 * @author Gaurav Sharma
 *
 */
public class Car {
	private String id;
	private String brand;
	private String fuel;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getBrand() {
		return brand;
	}

	public void setBrand(String brand) {
		this.brand = brand;
	}

	public String getFuel() {
		return fuel;
	}

	public void setFuel(String fuel) {
		this.fuel = fuel;
	}
}
